package Oop2.Learn.Composition;

public class Dimensions {
    private int width;
    private int heght;
    private int depth;

    public Dimensions(int width, int heght, int depth) {
        this.width = width;
        this.heght = heght;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHeght() {
        return heght;
    }

    public int getDepth() {
        return depth;
    }
}
